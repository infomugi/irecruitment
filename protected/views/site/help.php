<H3>
	Cara Login ke Sistem
</H3>
Untuk dapat masuk ke sistem, ketik alamat <code>www.jobinn.com</code> pada address bar browser, kemudian akan muncul tampilan form login seperti berikut:
<br><br>

<center>
<img src="images/help/login.png" class="img-responsive" width="50%">
</center>
<br><br>

<b>Uraian Cara Penggunaan:</b>
<ol>
	<li>Pada bagian Login form input untuk username dan password.</li>
	<li>Pengguna dapat mengisi username dan password pada field yang sudah disediakan.</li> 
	<li>Tombol login dipakai untuk masuk ke sistem dan jika datanya valid maka user akan masuk ke halaman menu utama</li>
	<li>Apabila tidak maka akan kembali ke form login.</li>
</ol>

